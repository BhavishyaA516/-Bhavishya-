public class RelationalOperators {
    public static void main(String[] args) {
        int x = 80;
        int y = 50;
        System.out.println(x<y);
        System.out.println(x>y);
        System.out.println(x>=y);
        System.out.println(x<=y);
        System.out.println(x==y);
    }
}

// both for Conditional {Logical operators} and Relational Operators will have outputs as BOOLEAN ==> TRUE & FALSE